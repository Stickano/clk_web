
            <h1>Continuous running <a name='Continuous running'></a></h1>

            <p>
Created Tuesday 25 December 2018
</p>

<p>
If you do not want to handle your boards via arguments, it is also possible to run the application as a continuously running program with a minimal user interface.
</p>

<p>
<b># Open the program as a continuous running application, with a simple user interface (no arguments):</b><br>
$ clk
</p>

<h2>Controls</h2>

<p>
You will have a visual representation of available option in the bottom of the program at all times. 
</p>

<h5>Cursor</h5>

<p>
There is a little cyan colored cursor (&gt;) showing the position on the board at all times. Use this cursor to navigate to the items you want to work on. 
</p>

<br>

<h5>Navigate</h5>

<p>
You can navigate with you arrow keys, or if you are familiar with the <i>Vi</i> editor, there is a few navigation options you might be acustomed to. 
</p>

<p>
<ul style='padding-left: 30pt'>
<li>Left / h          : Navigate left</li>
<li>Right / l         : Navigate right</li>
<li>Up / k            : Navigate up</li>
<li>Down / j          : Navigate down</li>
</ul>
</p>

<p>
<ul style='padding-left: 30pt'>
<li>Enter / Space  : Select</li>
<li>b                 : Back</li>
<li>q                 : Quit</li>
</ul>
</p>

<p>
<ul style='padding-left: 30pt'>
<li>r                 : Last in line</li>
<li>t                 : First in line</li>
<li>g                 : Last in list</li>
<li>f                 : First in list</li>
</ul>
</p>

<br>

<h5>Boards Overview</h5>

<p>
On the first page (boards overview), only a few options are available. 
</p>

<p>
<b># To create a new board, push the key: </b>i<br>
<b># To delete a board, navigate to the board and push key: </b>d<br>
<b># To update the name of a board, navigate to the board and push key: </b><i>u</i>
</p>

<br>

<h5>Show board</h5>

<p>
When you select a board, its lists will be shown. Navigate left/right to go between lists, and use up/down to through the cards in each list.
</p>

<p>
Here is a few restrictions:<br>
<ul style='padding-left: 30pt'>
<li>Only 3 lists are shown at any time - You can still get to them, by just continue to navigate next/back in your lists.</li>
<li>The cards will only be shown for the current selected list. This might be a thing to work on in the future, but for now you have to navigate through the lists to see its cards.</li>
<li>Only 5 cards will be shown at any time. Same as with the list, you can still navigate to further cards, by going down/up the available cards in the list.</li>
</ul>
</p>

<p>
<b># To create a new list, push key: </b>i<br>
<b># To create a new card, navigate to the wanted list and push key: </b>o
</p>

<p>
<b># To delete an item, navigate the cursor to either the list or card, and push key: </b>d<br>
<b># To update (rename) an item, likewise navigate to the item and push key: </b>u
</p>

<p>
<b># To show all available information about a board, push key: </b>m
</p>

<br>

<h5>Show card</h5>

<p>
When a card has been selected, its content will be shown. This includes checklists, and their checklist points, comments and a description. All card information is optional - Well, except the name I guess.
</p>

<p>
<b># To create a new checklist, push key:</b> i<br>
<b># To create a new checkist point, navigate to the wanted checklist and push key: </b>o<br>
<b># To check/un-check a point, navigate to the wanted point and push key: </b>Enter / Space
</p>

<p>
<b># To create a new comment push key: </b>c
</p>

<p>
<b># To delete an item, navigate the cursor to the item in question and push key: </b>d<br>
<b># To update (rename) an item, likewise navigate to the item and push key: </b>u
</p>

<br>

<h5>Cloud  options</h5>

<p>
To use the cloud options, one first have to be logged in.
</p>

<p>
<b># To login, push key: </b>a<br>
<b># When you are logged in, to see your profile page (and logout option), again push key: </b>a
</p>

<p>
<b># To save a board to the cloud, push key: </b>x<br>
<b># To get a list of all available boards in the cloud, push key: </b>z<br>
<b># To download a board, navigate to the wanted board in the list of available options and push key: </b>Enter / Space
</p>

<br>

<h5>Settings</h5>

<p>
The settings in this application is mainly On/Off settings, and if further information is needed, the user will be requestet for such. 
</p>

<p>
<b># To show the settings page, push key: </b>s<br>
<b># To activate/deactivate, navigate the items and push key: </b>Enter / Space
</p>

