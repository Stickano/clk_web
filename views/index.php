<h5>About</h5>

<p>
Command Line Kanban (CLK) is a powerful and versatile Kanban tool for your terminal. You can use it either as a continuous running application in your terminal, or you can initialize the program with arguments to quickly, and recursivly, administrate your boards and their items. CLK was created in late 2018 as a final project for a Computer Science (CS) graduation and will continue to be developed on as an opensource project, as there might be a small niche for the product. Besides the CLI application, a RESTful interface, running as an Azure web service and communicating with a database also hosted on Azure, and this website was created for the purpose of my finals.
</p>

<h5>How it works</h5>

<p>
Very basically, the program needs to know which item you are in/working on. The program has three "<i>layers</i>": <b>Boards</b>, <b>Lists</b> and <b>Cards</b>. If you are familiar with how Kanban boards work in general, this might all seem common to you.
</p>

<p>
So you.. <br>
    <ul>
        <li>go into a board (set the board) to see its lists, </li>
        <li>and you set the list to see its cards,</li>
        <li>and you set the card to see its content.</li>
    </ul>
</p>

<p>
Cards can hold content as <b>Checklists</b>, with checklist<b> Points</b>, <b>Comments</b> and a <b>Description</b>. This setup allows you to break a project into smaller pieces, giving a better overview of what has to be done for the project as a whole, and each piece (card) can hold the information on how to satisfy the requirements of each piece.
</p>

